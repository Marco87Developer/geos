  /// Constructs a [Country] instance starting from a *correctly formatted
  /// string*.
  ///
  /// This constructor is *not case sensitive* and *does not take into account
  /// leading and trailing white spaces*.
  ///
  /// Throws a [FormatException] if the [formattedString] does not contain a
  /// valid representation of a [Country].
  ///
  /// Any string that coincides with the properties [alpha2Code], [alpha3Code],
  /// [englishName], [flagEmoji] and [numericCode] of one of the possible
  /// [Country] instances are valid and unique representations.
  ///
  /// Examples:
  ///
  /// ```dart
  /// Country.parse('SC'); // Country.seychelles
  /// Country.parse('sc'); // Country.seychelles
  /// Country.parse(' sc '); // Country.seychelles
  ///
  /// Country.parse('PRY'); // Country.paraguay
  /// Country.parse('pry'); // Country.paraguay
  /// Country.parse(' pry '); // Country.paraguay
  ///
  /// Country.parse('Ã…land Islands'); // Country.alandIslands
  /// Country.parse('Ã¥land islands'); // Country.alandIslands
  /// Country.parse(' Ã¥land islands '); // Country.alandIslands
  ///
  /// Country.parse('ðŸ‡¦ðŸ‡¶'); // Country.antarctica
  /// Country.parse(' ðŸ‡¦ðŸ‡¶ '); // Country.antarctica
  ///
  /// Country.parse('184'); // Country.cookIslands
  /// Country.parse(' 184 '); // Country.cookIslands
  ///
  /// Country.parse(''); // Throws FormatException
  /// Country.parse('1234'); // Throws FormatException
  /// Country.parse('.'); // Throws FormatException
  /// ```
  ///
  factory Country.parse(final String formattedString) {
    final String lowerTrimmed = formattedString.toLowerCase().trim();
    for (final Country country in Country.values) {
      if (lowerTrimmed == country.alpha2Code.toLowerCase() ||
          lowerTrimmed == country.alpha3Code.toLowerCase() ||
          lowerTrimmed == country.englishName.toLowerCase() ||
          lowerTrimmed == country.flagEmoji ||
          lowerTrimmed == country.numericCode) {
        return country;
      }
    }
    throw FormatException(
      'This string ($formattedString) does not contain a valid and unique'
      ' representation of a [Country] instance.',
    );
  }

